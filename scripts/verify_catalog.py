#!/usr/bin/env python3
import sys,os
sys.path.insert(0,'scripts')
from decode_script import decode,render,load_tbl
rom=open("roms/Mini Yonku Let's & Go!! - Power WGP 2 (J) (NP).smc",'rb').read()
tbl=load_tbl("assets/translation_guide/glyph_table.tsv")
def foff(bank,addr): return ((bank&0x3F)<<16)|addr
def show(bank,ptrs,label):
    print(f"\n===== {label} (bank ${bank:02X}) — {len(ptrs)} pointers =====")
    for p in sorted(ptrs):
        o=foff(bank,p)
        # read until 0x00 term (cap 60 bytes)
        e=o
        while e<o+60 and rom[e]!=0x00:
            b=rom[e]; e+= 2 if (1<=b<=4 or b==7) else 1
        raw=rom[o:e+1]
        toks=decode(raw)
        unmapped=sum(1 for t in toks if t[0]=='glyph' and t[1] not in tbl)
        txt=render(toks,tbl)
        flag=' ***UNMAPPED***' if unmapped else ''
        print(f"  ${bank:02X}:{p:04X}  {render(toks,tbl)[:56]}{flag}")
